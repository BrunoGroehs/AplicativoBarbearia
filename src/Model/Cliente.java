package Model;

import java.util.Date;

/**
 *
 * @author Bruno Groehs
 */
public class Cliente extends Pessoa{
    private String cep;
    private String endereco;

    public Cliente(String cep, String endereco, String nome, char sexo, String dataNascimento, String telefone, String email, String rg) {
        super(nome, sexo, dataNascimento, telefone, email, rg);
        this.cep = cep;
        this.endereco = endereco;
    }

    public Cliente(String cep, String endereco, String nome) {
        super(nome);
        this.cep = cep;
        this.endereco = endereco;
    }
    

    public String getCep() {
        return cep;
    }

    public void setCep(String cep) {
        this.cep = cep;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public char getSexo() {
        return sexo;
    }

    public void setSexo(char sexo) {
        this.sexo = sexo;
    }

    public Date getDataNascimento() {
        return dataNascimento;
    }

    public void setDataNascimento(Date dataNascimento) {
        this.dataNascimento = dataNascimento;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getRg() {
        return rg;
    }

    public void setRg(String rg) {
        this.rg = rg;
    }

    @Override
    public String toString() {
        return getNome();
    }
    


}
